// Current language state
let currentLang = localStorage.getItem('lang') || 'ar';

/**
 * Initialize i18n
 */
function initI18n() {
    setLanguage(currentLang);

    // Attach event listeners to language toggle buttons
    document.querySelectorAll('.lang-toggle__btn').forEach(btn => {
        btn.addEventListener('click', () => {
            const lang = btn.dataset.lang;
            setLanguage(lang);
        });
    });
}

/**
 * Set the active language
 * @param {string} lang - Language code ('ar' or 'en')
 */
function setLanguage(lang) {
    currentLang = lang;
    localStorage.setItem('lang', lang);

    // Update HTML lang attribute and direction
    document.documentElement.lang = lang;
    document.documentElement.dir = lang === 'ar' ? 'rtl' : 'ltr';

    // Update all translatable elements
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.dataset.i18n;
        if (translations[lang][key]) {
            el.innerHTML = translations[lang][key];
        }
    });

    // Update placeholders
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
        const key = el.dataset.i18nPlaceholder;
        if (translations[lang][key]) {
            el.placeholder = translations[lang][key];
        }
    });

    // Update language toggle buttons
    document.querySelectorAll('.lang-toggle__btn').forEach(btn => {
        btn.classList.toggle('active', btn.dataset.lang === lang);
    });

    // Update WhatsApp link
    const whatsappLink = document.querySelector('.whatsapp-float a');
    if (whatsappLink) {
        const message = encodeURIComponent(translations[lang].whatsapp_message);
        whatsappLink.href = `https://wa.me/971504300221?text=${message}`;
    }
}

/**
 * Get translation by key
 * @param {string} key - Translation key
 * @returns {string} - Translated text
 */
function t(key) {
    return translations[currentLang][key] || key;
}

// Initialize when DOM is ready
document.addEventListener('DOMContentLoaded', initI18n);
